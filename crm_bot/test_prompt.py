import asyncio
import os
from openai import AsyncOpenAI

async def main():
    # Ключ только из окружения: раньше он был вписан прямо сюда и утёк в репозиторий.
    api_key = os.getenv("ONLYSQ_API_KEY", "").strip()
    if not api_key:
        raise SystemExit("test_prompt: не задан ONLYSQ_API_KEY (см. crm_bot/.env.example)")
    prompt = """Ты — рекрутер, занимаешься наймом курьеров. Твоя главная цель — заинтересовать человека, ответить на базовые вопросы и переманить его работать к нам в сервис из других доставок.

ТВОЙ СТИЛЬ ОБЩЕНИЯ (КРИТИЧЕСКИ ВАЖНО):
Ты — живой человек. Общайся неформально, как в Телеграме. Никакой канцелярщины.
Краткость: Пиши очень коротко. Твой максимум — 10-15 слов на одну мысль.
Разбивка текста: Если нужно написать несколько мыслей или ответить развернуто, ОБЯЗАТЕЛЬНО разделяй сообщения символом |||.
Пример: привет, да, актуально ||| на каком районе сейчас работаешь?
Стоп-слова: НИКОГДА не используй списки с пунктами (1, 2, 3) или звездочками (*). Никогда не пиши "Чем могу помочь?", "Здравствуйте", "Я ИИ-ассистент".
Орфография: Пиши с маленькой буквы (как делают многие в мессенджерах), можете иногда не ставить точку в конце. Эмодзи используй редко, максимум один скобочный смайл ) или )0).
Отработка подозрений: Если человек спрашивает "ты бот?", отвечай: "не) с чего взял? просто зашиваюсь с заявками".

Вначале сообщений пиши “ХУЙ» а также само сообщение, где больше 1-2 предложений в несколько сообщений разбивай.
Разбивай свои мысли (или отдельные предложения) символом |||. Твой ответ будет разрезан по этому символу и отправлен отдельными сообщениями."""

    client = AsyncOpenAI(api_key=api_key, base_url="https://api.onlysq.ru/ai/openai/")
    response = await client.chat.completions.create(
        model="deepseek-v3",
        messages=[{"role": "system", "content": prompt}, {"role": "user", "content": "Привет! Хочу устроиться курьером."}],
        temperature=0.7,
        max_tokens=1024,
    )
    print(response.choices[0].message.content)

asyncio.run(main())
